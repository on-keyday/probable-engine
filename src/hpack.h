    struct bitvec {
       private:
        std::string buf;
        size_t idx = 0;
        size_t pos = 0;

       public:
        bitvec() {
            buf.push_back(0);
        }

        void push_back(bool b) {
            if (pos == 8) {
                buf.push_back(0);
                idx++;
                pos = 0;
            }
            unsigned char v = b ? 1 : 0;
            buf[idx] |= (v << (7 - pos));
            pos++;
        }

        void append(const std::vector<bool>& a) {
            for (bool b : a) {
                push_back(b);
            }
        }

        size_t size() const {
            return idx * 8 + pos;
        }

        std::string& data() {
            return buf;
        }
    };

